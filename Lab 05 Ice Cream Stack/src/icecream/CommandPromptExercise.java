package icecream;

/**
 * Gain experience running Java programs from the command prompt by getting the
 * Strings below to print in the Command Prompt terminal.
 * 
 * From the src directory: To compile: javac
 * icecream\CommandPromptExercise.java To run: java
 * icecream.CommandPromptExercise
 *
 * @author Megan Rigsbee (mrigsbee)
 * @version 2015.04.21
 */
public class CommandPromptExercise {
    public static void main(String args[]) {
        String flavors = "My favorite ice cream flavors are:";
        String choco = "Chocolate";
        String van = "Vanilla";
        String mint = "Mint Chocolate Chip";

        System.out.println(flavors);
        System.out.println(choco);
        System.out.println(van);
        System.out.println(mint);
    }
}
